public class Race {
    private String season;
    private String round;
    private String url;
    private String raceName;
    private String date;
    private String time;

    public String getSeason() {
        return season;
    }

    public String getRound() {
        return round;
    }

    public String getUrl() {
        return url;
    }

    public String getRaceName() {
        return raceName;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }
}